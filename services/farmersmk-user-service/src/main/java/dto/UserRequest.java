package com.FarmersMK.user.dto;

public class UserRequest {

    private String name;
    private String email;
    private String role;
    private String password;

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getRole() {
        return role;
    }

    public String getPassword() {
        return password;
    }
}